package domain;

public class ProductV1 implements Product {
    private String id;
    private String name;

    public ProductV1(){
    }

    public ProductV1(String id, String name){
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}